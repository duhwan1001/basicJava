package e_oop;

public class Calculator {
	
	//+, -, *, /, % 연산을 수행하는 5개의 메서드를 만들어주세요.
	double add(double a, double b){
		return a + b;
	}
	
	double sub(double a, double b){
		return a - b;
	}
	
	double mul(double a, double b){
		return a * b;
	}
	
	double div(double a, double b){
		return a / b;
	}
	
	double mod(double a, double b){
		return a % b;
	}
	
	
}
